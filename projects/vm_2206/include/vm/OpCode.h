#pragma once

/**
 * @file OpCode.h
 * @brief 虚拟机指令操作码定义
 *
 * 定义了虚拟机支持的所有指令操作码
 * 采用强类型枚举（C++11）确保类型安全
 */

// 指令操作码枚举
enum class OpCode : int {
    READ = 10,      // 从终端读取一个整数到指定内存地址
    WRITE = 11,     // 将指定内存地址的值输出到终端
    LOAD = 20,      // 将指定内存地址的值加载到累加器
    STORE = 21,     // 将累加器的值存储到指定内存地址
    ADD = 30,       // 累加器 += 内存[地址]
    SUB = 31,       // 累加器 -= 内存[地址]
    DIV = 32,       // 累加器 /= 内存[地址]
    MUL = 33,       // 累加器 *= 内存[地址]
    JMP = 40,       // 无条件跳转到指定地址
    JMPNEG = 41,    // 如果累加器为负则跳转
    JMPZERO = 42,   // 如果累加器为零则跳转
    HALT = 43       // 停机，程序结束
};
